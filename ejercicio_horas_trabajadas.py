horas_trabajadas = float(input("Introduzca el numero de horas trabajadas: "))
coste_por_hora = float(input("Ingrese el coste que recibe por hora trabajada: "))

dinero_recibido = horas_trabajadas * coste_por_hora
dinero_recibido = str(dinero_recibido)
print("El total de dinero recibido por sus horas laboradas es de: " + dinero_recibido)
