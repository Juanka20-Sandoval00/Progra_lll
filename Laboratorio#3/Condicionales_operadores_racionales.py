"""Condicionales (if/else/elif) y Operadores Relacionales:
Crea un programa que determine si un nÃºmero ingresado por el usuario es positivo, negativo o cero."""
num = float(input("Ingrese un valor "))

if num > 0 :
    print  ("El numero ingresado es positivo ")

elif num < 0:
    print("El numero ingresado es negativo. ")

else:
    print("El numero ingresado es cero ")
