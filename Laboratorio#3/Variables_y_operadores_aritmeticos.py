"""Variables y operadores aritmeticos
Escribe un programa que solicite al usuario ingresar dos números y luego muestre la suma, resta, multiplicación 
y división de esos números. """

num1 = float(input("Ingrese el primer valor: "))
num2 = float(input("Ingrese el segundo valor: "))

suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
print(f"La suma de {num1} + {num2} es: ", suma)
print(f"La resta de {num1} - {num2} es: ", resta)
print(f"La multiplicacion de {num1} * {num2} es: ", multiplicacion)
print(f"La division de {num1} / {num2} es: ", division)

